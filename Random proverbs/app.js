const content = document.querySelector(".proverb-content");
const book = document.querySelector(".book-title");
const funFact = document.querySelector(".fun-facts")

const randomBtn = document.querySelector(".btn");
const surpriseBtn = document.querySelector(".surprise-btn");
const prevBtn = document.querySelector(".prev-btn");
const nextBtn = document.querySelector(".next-btn");



let currFact = 0;
let currProverb = 0;


const showProverb = (words) => {
  const arrItem = proverbs[words];
  content.textContent = arrItem.proverb;
  book.textContent = arrItem.title;
};

const showFact = (words) => {
  const facts = funFacts[words];
  funFact.textContent = facts.fact

}

window.addEventListener("DOMContentLoaded", () => {
  showProverb(currProverb);
})

window.addEventListener("DOMContentLoaded", () => {
  showFact(currFact)
})


randomBtn.addEventListener("click", () => {
currFact++;
if (currFact > funFacts.length - 1) {
  currFact = 0
}
  showFact(currFact)
})


nextBtn.addEventListener("click", () => {
  currProverb++;
  if (currProverb > proverbs.length - 1) {
    currProverb = 0;
  }
  showProverb(currProverb);
});
prevBtn.addEventListener("click", () => {
  currProverb--;
  if (currProverb < 0) {
    currProverb = proverbs.length - 1;
  }
  showProverb(currProverb);
});

surpriseBtn.addEventListener("click", () => {
currProverb = Math.floor(Math.random() * proverbs.length);
showProverb(currProverb);
});


